export const getNovelChapters = (novelId: number) => {
  // Dữ liệu giả cho 50 chương
  const chapters = Array.from({ length: 50 }, (_, i) => ({
    id: i + 1,
    novelId: novelId,
    title: `Chương ${i + 1}: ${getChapterTitle(i + 1)}`,
    wordCount: Math.floor(Math.random() * 2000) + 1500,
    publishDate: new Date(Date.now() - (50 - i) * 24 * 60 * 60 * 1000).toLocaleDateString('vi-VN'),
    isRead: Math.random() > 0.7,
    isNew: i >= 47
  }));

  function getChapterTitle(chapterNum: number): string {
    const titles = [
      "Khởi đầu hành trình", "Gặp gỡ sư phụ", "Bài học đầu tiên", "Thử thách khó khăn",
      "Đồng môn kết nghĩa", "Bí mật võ công", "Trận đấu đầu tiên", "Thất bại đau đớn",
      "Rèn luyện khổ cực", "Đột phá giới hạn", "Giang hồ hiểm ác", "Anh hùng cứu mỹ",
      "Tình yêu đầu đời", "Âm mưu thâm sâu", "Báo thù gia tộc", "Sức mạnh mới",
      "Đại hội võ lâm", "Kẻ thù cũ", "Liên minh bất ngờ", "Phản bội đau lòng"
    ];
    return titles[chapterNum % titles.length] || `Phần ${chapterNum}`;
  }

  return chapters;
};

